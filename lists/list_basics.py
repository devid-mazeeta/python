# defining the list
list1 = ['a','b','c','d']
print(list1) # ['a', 'b', 'c', 'd']

# copying the list
list2 = list1
print(list2) # ['a', 'b', 'c', 'd']

# accessing the data from the list using index (or) string slicing concept
print(list1[0]) # a
print(list1[2]) # c
print(list1[-2]) # c
print(list1[1:]) # ['b', 'c', 'd']
print(list1[1:2]) # ['b']
print(list1[-2:]) # ['c', 'd']
print(list1[:]) # ['a', 'b', 'c', 'd']
print(list1[:3]) # ['a', 'b', 'c']
