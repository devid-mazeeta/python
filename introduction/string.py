# printing string
name_1 = 'python'
print(name_1)

# finding length of the string
name_2 = 'language'
print(len(name_2))

# adding string to the string
output = name_1 + name_2
print(output)

# adding integer to the string | integer converted to string type
output = name_1 + ' ' + str(len(name_2))
print (output)

